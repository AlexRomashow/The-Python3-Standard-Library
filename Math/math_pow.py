import math

INPUTS = [
    # Типичное использование
    (2, 3),
    (2.1, 3.2),

    # Всегда 1
    (1.0, 5),
    (2.0, 0),
    
    # Не число
    (2, float('nan')),

    # Корни
    (9.0, 0.5),
    (27.0, 1.0 / 3),
]

for x, у in INPUTS:
    print('{:5.1f} ** {:5.3f} = {:6.3f}'.format(
    x, у, math.pow(x, у)))
